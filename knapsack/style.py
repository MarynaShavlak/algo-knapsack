"""Єдина візуальна тема для всіх візуалізацій: палітра кольорів і параметри matplotlib.

Усі кольори зведено в одне місце іменованими константами, щоб змінити вигляд
можна було з однієї точки. Семантика підсвічувань спільна для таблиці ДП,
перебору підмножин і кодових панелей: зелений — «взяти», синій — «не брати /
значення зверху», помаранчевий — поточна клітинка, жовтий — шлях відновлення.
"""

from __future__ import annotations

import matplotlib.pyplot as plt

# ---------------------------------------------------------------------------
# Параметри фігур
# ---------------------------------------------------------------------------
#: DPI для всіх збережених/показаних фігур.
FIGURE_DPI: int = 110
#: DPI для GIF-анімацій (нижчий за статичні рисунки — менший розмір файлу).
GIF_DPI: int = 90
#: Базовий розмір шрифту matplotlib.
BASE_FONT_SIZE: int = 11
#: Сімейство шрифтів (містить кирилицю та стрілки →).
FONT_FAMILY: str = "DejaVu Sans"

# ---------------------------------------------------------------------------
# Палітра: заливки та обведення
# ---------------------------------------------------------------------------
#: 🟩 гілка «взяти»: заливка клітинки-джерела / текст; також предмет у рюкзаку.
GREEN_FILL, GREEN_EDGE, GREEN_TXT = "#C8E6C9", "#2E7D32", "#2E7D32"
#: 🟦 гілка «не брати» (значення зверху): заливка джерела / обведення.
BLUE_FILL, BLUE_EDGE = "#E3F2FD", "#1976D2"
#: 🟧 поточна клітинка ``(i, w)`` під час покрокового заповнення.
CURRENT_FILL, CURRENT_EDGE = "#FFE0B2", "#E65100"
#: 🟨 клітинка шляху відновлення (зворотний прохід по таблиці).
PATH_FILL, PATH_EDGE = "#FFF59D", "#F9A825"
#: 🔴 акцент відповіді (рамка клітинки ``K[n][W]``, найкраща підмножина).
ANSWER_EDGE = "#D32F2F"
#: Колір сітки таблиці.
GRID_EDGE = "#CFCFCF"
#: Заливка ще не заповнених клітинок таблиці.
EMPTY_FILL = "#F7F7F7"
#: Заливка базового рядка/стовпця (i = 0 або w = 0).
BASE_FILL = "#FAFAFA"
#: Звичайний предмет на рисунках (смуги ваги, картки предметів).
ITEM_COLOR = "#1f77b4"
#: Порожнє місце в рюкзаку (смуги заповнення).
SLACK_FILL = "#E0E0E0"
#: Нейтральний сірий: другорядні елементи, відкинуті варіанти.
NEUTRAL_GRAY = "#9E9E9E"

# ---------------------------------------------------------------------------
# Палітра: підсвічування кодової панелі (ліва панель «код ↔ таблиця»)
# ---------------------------------------------------------------------------
# Колір кодує СЕМАНТИКУ гілки, що спрацювала на цьому кроці, а не лише «активність»:
#: 🟨 рядок коду, що виконується зараз (цикли / перевірка умови).
HL_ACTIVE = "#FFF3C4"
#: 🟩 спрацювала гілка «взяти»: вартість предмета увійшла в клітинку.
#: Навмисно гармонує з GREEN_FILL клітинки таблиці — зелений рядок коду ↔ зелена клітинка.
HL_TAKE = "#C8E6C9"
#: 🟥 значення просто перенесено зверху: «не брати» перемогло або предмет не влазить.
HL_COPY = "#FFCDD2"

# ---------------------------------------------------------------------------
# Палітра: текстові відтінки
# ---------------------------------------------------------------------------
#: Підписи рядків/стовпців (заголовки таблиці).
HEADER_TXT = "#555"
#: Другорядні підписи у формулі (``K[i][w]`` тощо).
SUBLABEL_TXT = "#777"
#: Приглушений сірий (нулі базового рядка, підписи, примітки).
MUTED_TXT = "#9aa0a6"
#: Звичайне (темне) значення клітинки.
TEXT_DARK = "#222"
#: Числа у розгорнутій формулі кроку.
TEXT_FORMULA = "#3c4043"
#: Результат формули (жирний підсумок клітинки).
TEXT_RESULT = "#1a1a1a"


def configure_style() -> None:
    """Налаштовує глобальні параметри matplotlib (DPI, шрифт із кирилицею).

    Попередження «надто багато відкритих фігур» навмисно НЕ вимикаємо: фігури
    закривають ``save_figure`` (одразу після збереження) та збірка анімацій
    (під час рендера кадрів), а найбільша пачка кадрів до зшивання — 15 —
    менша за типовий поріг попередження. Якщо воно колись з'явиться, це
    чесний сигнал про витік, а не шум.
    """
    plt.rcParams["figure.dpi"] = FIGURE_DPI
    plt.rcParams["font.size"] = BASE_FONT_SIZE
    plt.rcParams["font.family"] = FONT_FAMILY
