"""Дані прикладів — джерело правди для навчальних інстансів задачі.

Ті самі ваги, вартості й місткості використовують усі скрипти ``examples/``
та README, тож для прикладів числа визначені лише тут. Тести **навмисно**
тримають власні копії цих чисел (``tests/test_core.py`` лишається без важких
залежностей, а імпорт ``_items`` тягне ``_common`` → matplotlib); збіг копій
із цим файлом стереже окремий smoke-тест. Кожен інстанс —
:class:`_common.KnapsackExample`.

* :data:`CLASSIC` — інстанс із конспекту: 3 предмети, місткість 50.
  Оптимум — **220** (предмети П2 + П3, вага рівно 50). Саме на ньому жадібний
  метод помиляється (дає 160) — головний контрприклад розбору.
* :data:`SMALL` — малий інстанс із конспекту: 3 предмети, місткість 4.
  Таблиця ДП лише 4×5 — кожну клітинку видно на рисунку. Оптимум — **18**
  (П1 + П3). Цікавий тим, що П3 у клітинці ``K[3][3]`` влазить, але брати його
  невигідно.
* :data:`BIG` — більший інстанс (20 предметів): показує «вибух» повного
  перебору. 2²⁰ = 1 048 576 підмножин проти 21 × 61 = 1 281 клітинки ДП.
  Числа зафіксовано літералами, щоб результати були відтворюваними.
"""

from __future__ import annotations

from _common import KnapsackExample

#: Класичний інстанс із конспекту (брутфорс/ДП → 220; жадібний → лише 160).
CLASSIC = KnapsackExample(
    slug="classic",
    label="класичний",
    weights=[10, 20, 30],
    values=[60, 100, 120],
    capacity=50,
)

#: Малий інстанс із конспекту — для покрокового розбору таблиці ДП (відповідь 18).
SMALL = KnapsackExample(
    slug="small",
    label="малий",
    weights=[1, 2, 3],
    values=[6, 10, 12],
    capacity=4,
)

#: Більший інстанс: 2²⁰ підмножин — повний перебір уже відчутно «думає»,
#: а ДП відповідає миттєво (обидва дають однакову відповідь — це звіряє тест).
BIG = KnapsackExample(
    slug="big",
    label="великий",
    weights=[12, 7, 11, 8, 9, 5, 14, 6, 10, 4, 13, 3, 15, 2, 8, 6, 11, 5, 9, 7],
    values=[40, 35, 50, 32, 41, 25, 56, 30, 38, 19, 45, 14, 58, 11, 33, 27, 44, 22, 37, 31],
    capacity=60,
)
